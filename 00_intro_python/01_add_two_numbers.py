# Prompting the user for input
question_1 = 'Enter the first number: '
question_2 = 'Enter the second number: '

# Collecting and converting input to integers
first_number = int(input(question_1))
second_number = int(input(question_2))

# Calculating the sum
total_sum = first_number + second_number

# Displaying the result
print(f"The total of {first_number} and {second_number} is {total_sum}.")
