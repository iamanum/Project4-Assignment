print("LET'S CALCULATE THE NUMBER OF SECONDS IN A YEAR")

# Define constants for time units
days_in_year = 365
hours_per_day = 24
minutes_per_hour = 60
seconds_per_minute = 60

# Calculate total seconds in a year
total_seconds_in_year = days_in_year * hours_per_day * minutes_per_hour * seconds_per_minute

# Output the result
print(f'There are {total_seconds_in_year} seconds in a year')
